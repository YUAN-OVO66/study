public class yunsuanfu2 {
    public static void main(String[] args) {

        //自增减
        int a=10;
        a++;//a=a+1 ++a
        System.out.println(a);
        a--;//a=a-1 --a
        System.out.println(a);//自增减只能操作于变量不能操作字面量
        int i=10;
        int rs=++i;
        System.out.println(rs);
        System.out.println(i);

        int j=10;
        int rs2=j++;
         System.out.println(rs2);
          System.out.println(j);

    }
    
}
