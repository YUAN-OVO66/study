public class yufa {
    public static void main(String[] args){
        System.out.println('a'+10);//97+10=107
        System.out.println('A'+10);//65+10=75
        System.out.println('0'+10);//48+10=58
        //掌握基本数据类型使用
        byte a=127;
        short s = 12345;
        int i= 42000;
        //long d=100000000000;//超过int范围
        //随便写一个整形自变量是long需要在后面加l或L
        long d=100000000000L ;
        float f=3.14F;
        //默认随便小数是double float后用f/F
        //char可存字符
        char ch='a';
        //boolean 存真假
        boolean z=true;
        boolean j=false;
        //拓展引用数据类型
        //String称为字符串类型，定义的变量可以用于记住一个字符串数据
        String name ="张三";
        System.out.println(name);


        

    }
}
